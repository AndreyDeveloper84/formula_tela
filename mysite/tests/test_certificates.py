@pytest.mark.django_db
class TestCertificateCheckAPI:
    url = "/api/certificates/check/"

    def test_valid_certificate(self, client, gift_certificate):
        resp = client.get(f"{self.url}?code={gift_certificate.code}")
        assert resp.status_code == 200
        data = resp.json()
        assert data["success"] is True
        assert data["certificate"]["code"] == gift_certificate.code
        assert data["certificate"]["valid"] is True
        assert data["certificate"]["remaining_value"] == "3000"

    def test_not_found(self, client):
        resp = client.get(f"{self.url}?code=INVALID-CODE")
        assert resp.status_code == 404
        assert resp.json()["success"] is False

    def test_empty_code(self, client):
        resp = client.get(self.url)
        assert resp.status_code == 400


