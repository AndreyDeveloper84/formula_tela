@pytest.mark.django_db
class TestOrderModel:
    def test_order_number_autogenerated(self):
        order = baker.make(
            "services_app.Order",
            order_type="certificate",
            client_name="Тест",
            client_phone="+70001112233",
        )
        assert order.number.startswith("FT-")
        assert len(order.number) == 16  # FT-YYYYMMDD-XXXX

    def test_order_number_unique(self):
        o1 = baker.make("services_app.Order", order_type="certificate",
                        client_name="A", client_phone="1")
        o2 = baker.make("services_app.Order", order_type="certificate",
                        client_name="B", client_phone="2")
        assert o1.number != o2.number

    def test_order_str(self, order):
        assert "FT-" in str(order)
        assert "Сертификат" in str(order)


@pytest.mark.django_db
class TestGiftCertificateModel:
    def test_code_autogenerated(self, gift_certificate):
        assert gift_certificate.code.startswith("FT-")
        assert len(gift_certificate.code) == 12  # FT-XXXX-XXXX

    def test_is_valid_paid(self, gift_certificate):
        assert gift_certificate.is_valid is True

    def test_is_valid_pending(self, gift_certificate):
        gift_certificate.status = "pending"
        assert gift_certificate.is_valid is False

    def test_is_valid_expired_date(self, gift_certificate):
        gift_certificate.valid_until = date.today() - timedelta(days=1)
        assert gift_certificate.is_valid is False

    def test_remaining_value_full(self, gift_certificate):
        assert gift_certificate.remaining_value == Decimal("3000")

    def test_remaining_value_after_redemption(self, gift_certificate):
        baker.make(
            "services_app.CertificateRedemption",
            certificate=gift_certificate,
            amount=Decimal("1500"),
            service_name="Массаж",
            redeemed_by="Клиент",
        )
        assert gift_certificate.remaining_value == Decimal("1500")

    def test_remaining_value_service_type(self, gift_certificate):
        gift_certificate.certificate_type = "service"
        assert gift_certificate.remaining_value is None

    def test_str(self, gift_certificate):
        s = str(gift_certificate)
        assert "FT-" in s
        assert "3000" in s


